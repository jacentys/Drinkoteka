
-- ============================================================
-- DRINKOTHÈQUE — schemat bazy danych
-- ============================================================

create table if not exists ingredients (
  id          text primary key,
  name        text not null,
  category    text,
  alcohol_pct int default 0,
  color       text,
  photo       text,
  description text,
  calories    int default 0,
  unit        text,
  url         text
);

create table if not exists ingredient_substitutes (
  ingredient_id  text references ingredients(id) on delete cascade,
  substitute_id  text references ingredients(id) on delete cascade,
  primary key (ingredient_id, substitute_id)
);

create table if not exists drinks (
  id          text primary key,
  name        text not null,
  category    text,
  source      text,
  color       text,
  photo       text,
  alcohol_pct int default 0,
  sweetness   text,
  glass       text,
  strength    text,
  favorite    boolean default false,
  note        text,
  remarks     text,
  url         text,
  calories    int default 0,
  recommended boolean default false,
  user_id     uuid references auth.users(id) on delete cascade
);

create table if not exists drink_ingredients (
  id            bigint generated always as identity primary key,
  drink_id      text references drinks(id) on delete cascade,
  ingredient_id text references ingredients(id) on delete cascade,
  amount        numeric,
  unit          text,
  info          text,
  optional      boolean default false,
  sort_order    int default 0
);

create table if not exists drink_steps (
  id         bigint generated always as identity primary key,
  drink_id   text references drinks(id) on delete cascade,
  step_no    int,
  description text,
  optional   boolean default false
);

create table if not exists drink_spirits (
  drink_id   text references drinks(id) on delete cascade,
  spirit     text,
  primary key (drink_id, spirit)
);

-- RLS: przepisy IBA widoczne dla wszystkich, własne tylko dla właściciela
alter table drinks enable row level security;
create policy "IBA drinks are public" on drinks for select using (user_id is null);
create policy "Own drinks visible to owner" on drinks for select using (auth.uid() = user_id);
create policy "Insert own drinks" on drinks for insert with check (auth.uid() = user_id);
create policy "Update own drinks" on drinks for update using (auth.uid() = user_id);
create policy "Delete own drinks" on drinks for delete using (auth.uid() = user_id);

alter table ingredients enable row level security;
create policy "Ingredients are public" on ingredients for select using (true);

alter table ingredient_substitutes enable row level security;
create policy "Substitutes are public" on ingredient_substitutes for select using (true);

alter table drink_ingredients enable row level security;
create policy "Drink ingredients are public" on drink_ingredients for select using (true);

alter table drink_steps enable row level security;
create policy "Drink steps are public" on drink_steps for select using (true);

alter table drink_spirits enable row level security;
create policy "Drink spirits are public" on drink_spirits for select using (true);

